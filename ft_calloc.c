#include "libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	void	*ptr;
	size_t	space;

	space = size * count;
	if (!(ptr = malloc(space)))
		return (NULL);
	ft_bzero(ptr, space);
	return (ptr);
}
